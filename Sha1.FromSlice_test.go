package hashes

import (
	"bytes"
	"testing"
)

func TestSha1_FromSlice(t *testing.T) {

	t.Run("Test FromSlice() Happy", func(t *testing.T) {
		var hash Sha1
		actual := []byte{
			0x00, 0x01, 0x02, 0x03, 0x04,
			0x05, 0x06, 0x07, 0x08, 0x09,
			0x0A, 0x0B, 0x0C, 0x0D, 0x0E,
			0x0F, 0x10, 0x11, 0x12, 0x13,
		}
		expected := [20]byte{
			0x00, 0x01, 0x02, 0x03, 0x04,
			0x05, 0x06, 0x07, 0x08, 0x09,
			0x0A, 0x0B, 0x0C, 0x0D, 0x0E,
			0x0F, 0x10, 0x11, 0x12, 0x13,
		}
		hash.FromSlice(actual)
		if !bytes.Equal(actual, expected[:]) {
			t.Fatalf("Mismatch\n"+
				"Actual:  %v\n"+
				"Expected:%v\n",
				actual, expected)
		}
	})

	t.Run("Test FromSlice() Truncating overflow", func(t *testing.T) {
		var hash Sha1
		actual := []byte{
			0x00, 0x01, 0x02, 0x03, 0x04,
			0x05, 0x06, 0x07, 0x08, 0x09,
			0x0A, 0x0B, 0x0C, 0x0D, 0x0E,
			0x0F, 0x10, 0x11, 0x12, 0x13,
			0xFF, 0xFF, 0xFF, 0xFF, 0xFF, //These are overflow
		}
		expected := [20]byte{
			0x00, 0x01, 0x02, 0x03, 0x04,
			0x05, 0x06, 0x07, 0x08, 0x09,
			0x0A, 0x0B, 0x0C, 0x0D, 0x0E,
			0x0F, 0x10, 0x11, 0x12, 0x13,
		}
		hash.FromSlice(actual)
		if !bytes.Equal(actual[:20], expected[:]) {
			t.Fatalf("Mismatch\n"+
				"Actual:  %v\n"+
				"Expected:%v\n",
				actual, expected)
		}
	})

	t.Run("Test FromSlice() Empty Slice", func(t *testing.T) {
		var hash Sha1
		actual := []byte{
			0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00,
		}
		var expected [20]byte //leave it empty
		hash.FromSlice([]byte{})
		if !bytes.Equal(actual[:], expected[:]) {
			t.Fatalf("Mismatch\n"+
				"Actual:  %v\n"+
				"Expected:%v\n",
				actual, expected)
		}
	})

	t.Run("Test FromSlice() nil Slice", func(t *testing.T) {
		var hash Sha1
		actual := []byte{
			0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00,
		}
		var expected [20]byte //leave it empty
		hash.FromSlice(nil)
		if !bytes.Equal(actual[:], expected[:]) {
			t.Fatalf("Mismatch\n"+
				"Actual:  %v\n"+
				"Expected:%v\n",
				actual, expected)
		}
	})
}
